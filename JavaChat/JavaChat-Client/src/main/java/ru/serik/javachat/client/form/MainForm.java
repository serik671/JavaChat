    private void sendMessage(){
        if(jTextFieldMessage.getText().isBlank()){
            return;
        }
        try{
            client.sendMessage(jTextFieldMessage.getText());
            jTextAreaLog.append(String.format("%s: Сообщение отправлено\n", LocalDateTime.now()));
            jTextFieldMessage.setText("");
        }catch(Exception e){
            jTextAreaLog.append(String.format("%s: Message Error: %s\n", LocalDateTime.now(), e.getMessage()));
        }
    }
    
    private void setDisconnectedState(){
        jButtonSend.setEnabled(false);
        jTextFieldMessage.setEnabled(false);
        jTextFieldUsername.setEnabled(true);
        jTextFieldHost.setEnabled(true);
        jSpinnerPort.setEnabled(true);
        jButtonConnection.setText("Подключиться");
        connect = false;
    }
    
    private void setConnectedState(){
        jTextAreaLog.append(String.format("%s: Успешное подключение к серверу\n", LocalDateTime.now()));
        jButtonSend.setEnabled(true);
        jTextFieldMessage.setEnabled(true);
        jTextFieldUsername.setEnabled(false);
        jTextFieldHost.setEnabled(false);
        jSpinnerPort.setEnabled(false);
        jButtonConnection.setText("Отключиться");
        connect = true;
    }
    
