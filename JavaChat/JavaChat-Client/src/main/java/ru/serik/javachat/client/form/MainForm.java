    private void jButtonConnectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConnectionActionPerformed
        try{
            if(connect){
                client.disconnect();
                setDisconnectedState();
                jTextAreaLog.append(String.format("%s: Соединение с сервером разорвано\n", LocalDateTime.now()));
            }else{
                String username = jTextFieldUsername.getText().strip();
                if(!username.matches("[A-я0-9]+")){
                    JOptionPane.showMessageDialog(this, "Некорректное имя пользователя", APP_NAME, JOptionPane.WARNING_MESSAGE);
                    username = "";
                    jTextFieldUsername.setText(username);
                }
                if(username.isEmpty()){
                    client.connect(jTextFieldHost.getText(), (int) jSpinnerPort.getValue());
                }else{
                    client.connect(jTextFieldHost.getText(), (int) jSpinnerPort.getValue(), jTextFieldUsername.getText());
                }
                setConnectedState();
            }
        }catch(Exception e){
            jTextAreaLog.append(String.format("%s: Error: %s\n", LocalDateTime.now(), e.getMessage()));
        }
    }//GEN-LAST:event_jButtonConnectionActionPerformed

